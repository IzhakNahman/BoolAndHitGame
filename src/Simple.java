public class Simple{
	
	 private int x, y, Height, Width;

	 
	public Simple(int x, int y, int Height, int Width) 
	{
		this.x = x;
		this.y = y;
		this.Height = Height;
		this.Width = Width;
	}
	public int getX() 
	{
		return x;
	}
	public void setX(int x) 
	{
		this.x = x;
	}
	public int getY() 
	{
		return y;
	}
	public void setY(int y) 
	{
		this.y = y;
	}
	public int getHeight() 
	{
		return Height;
	}
	public void setHeight(int Height) 
	{
		this.Height = Height;
	}
	public int getWidth() 
	{
		return Width;
	}
	public void setWidth(int Width) 
	{
		this.Width = Width;
	}

	
}
